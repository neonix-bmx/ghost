#include "libac97/ac97.hpp"

#include <ghost.h>

bool ac97OpenChannel(g_ac97_channel* channel)
{
	if(!channel)
		return false;

	g_fd fd = g_open_f(AC97_DEVICE_PATH, G_FILE_FLAG_MODE_WRITE | G_FILE_FLAG_MODE_BINARY);
	if(fd == G_FD_NONE)
		return false;

	channel->pcmPipe = fd;
	return true;
}
